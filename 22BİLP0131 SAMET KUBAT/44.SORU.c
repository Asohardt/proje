#include <stdio.h>

int main()
{
int i;




printf("1'den 25'e ilerleyen sayi dizisi:\n");
for (i = 1; i <= 25; i++)
{
    printf("%d ", i);
}
printf("\n");


printf("25'den 1'e geri dönen sayi dizisi:\n");
for (i = 25; i >= 1; i--)
{
    printf("%d ", i);
}
printf("\n");

return 0;
}
